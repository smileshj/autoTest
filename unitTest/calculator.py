class Math:
    def __init__(self,a,b):
        self.a=int(a)
        self.b=int(b)

    #加法
    def add(self):
        return self.a+self.b

    #减法
    def minus(self):
        return self.a-self.b

    #乘法
    def multiplication(self):
        return self.a * self.b

    #除法
    def division(self):
        return self.a / self.b